<h2>RepairsLab</h2>
<p lang="en-US"><font size="2">The system
RepeairsLab allow to manage
the entry and exit of equipment repair in a small workshop for
repairs or service center.</font></p>
<p lang="en-US"><font size="2">The
application allows the printing of a
"Repairs sheet" for each device and manage the state of
repair. Inside the Repairs ticket, you can enter the features of the
delivered and the state of repair. </font>
</p>
<p lang="en-US"><font size="2">The system
provides management of
different master data for definition of states of repair, customers,
types of equipment, trademarks and models.</font></p>
<p lang="en-US"><font size="2">You can print
a paper "Repairs
sheet" in duplicate to store paper and as a delivery receipt for
the customer.</font></p>
